#!/usr/bin/env bash
# =============================================================================
# setup.sh — Install devco-agent-skills (scope-aware)
# =============================================================================
#
# Detects installation scope automatically:
#
#   USER SCOPE (plugin installed via marketplace/npm — PLUGIN_DIR != PROJECT_ROOT):
#     - CLAUDE.md, rules   → ~/.claude/          (global, every session)
#     - hooks              → ~/.claude/settings.json with ${CLAUDE_PLUGIN_ROOT}
#     - memory             → .claude/memory/     (project scope, always)
#
#   PROJECT SCOPE (plugin repo IS the project — PLUGIN_DIR == PROJECT_ROOT):
#     - CLAUDE.md, rules   → .claude/            (project scope)
#     - hooks              → .claude/settings.json with $CLAUDE_PROJECT_DIR
#     - memory             → .claude/memory/     (project scope)
#
# Usage:
#   bash scripts/setup.sh              # Auto-detect scope
#   bash scripts/setup.sh --update     # Refresh (idempotent re-run)
#
# Safe to re-run: idempotent via start/end markers.
# =============================================================================

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PLUGIN_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"

MARKER_START="<!-- devco-agent-skills:start -->"
MARKER_END="<!-- devco-agent-skills:end -->"

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

ok()   { echo "  ✅  $*"; }
info() { echo "  →   $*"; }
warn() { echo "  ⚠️   $*"; }
h()    { echo ""; echo "── $* ──────────────────────────────────────────"; }

# Inject plugin content into a target file using markers (idempotent)
inject_plugin_content() {
  local target="$1"
  local content_file="$2"

  touch "$target"

  # Remove existing block
  if grep -q "$MARKER_START" "$target" 2>/dev/null; then
    local tmpfile
    tmpfile="$(mktemp)"
    awk "
      /$MARKER_START/ { skip=1 }
      !skip           { print }
      /$MARKER_END/   { skip=0 }
    " "$target" > "$tmpfile"
    mv "$tmpfile" "$target"
    info "Removed previous devco-agent-skills block from $(basename "$target")"
  fi

  # Append fresh block
  {
    printf '\n'
    echo "$MARKER_START"
    echo "<!-- Generated by devco-agent-skills/scripts/setup.sh $(date -u '+%Y-%m-%dT%H:%MZ') -->"
    printf '\n'
    cat "$content_file"
    printf '\n'
    echo "$MARKER_END"
    printf '\n'
  } >> "$target"

  local block_lines
  block_lines="$(awk "/$MARKER_START/{found=1} found{c++} /$MARKER_END/{found=0} END{print c}" "$target")"
  echo "$block_lines"
}

# Write hooks JSON with a given path prefix
write_hooks_json() {
  local target_file="$1"
  local hook_path_prefix="$2"  # e.g., "${CLAUDE_PLUGIN_ROOT}" or "$CLAUDE_PROJECT_DIR"

  cat > "$target_file" <<HOOKJSON
{
  "_comment": "devco-agent-skills-hooks — auto-generated by /setup. Safe to re-run.",
  "extraKnownMarketplaces": {
    "devco-agent-skills": {
      "source": {
