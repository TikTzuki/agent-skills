          }
        ]
      }
    ],
    "PreCompact": [
      {
        "hooks": [
          {
            "type": "command",
            "command": "${hook_path_prefix}/scripts/hooks/pre-compact.sh",
            "timeout": 30,
            "async": true
          }
        ]
      }
    ],
    "Stop": [
      {
        "hooks": [
          {
            "type": "command",
            "command": "${hook_path_prefix}/scripts/hooks/check-debug-statements.sh",
            "timeout": 15
          },
          {
            "type": "command",
            "command": "${hook_path_prefix}/scripts/hooks/cost-tracker.sh",
            "timeout": 10,
            "async": true
          },
