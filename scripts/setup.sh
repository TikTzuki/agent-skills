        "repo": "taipt1504/agent-skills"
      }
    }
  },
  "enabledPlugins": {
    "devco-agent-skills@devco-agent-skills": true
  },
  "hooks": {
    "SessionStart": [
      {
        "hooks": [
          {
            "type": "command",
            "command": "${hook_path_prefix}/scripts/hooks/session-start.sh",
            "timeout": 30
          }
        ]
      }
    ],
    "PreToolUse": [
      {
        "matcher": "Edit|Write|MultiEdit",
        "hooks": [
          {
            "type": "command",
            "command": "${hook_path_prefix}/scripts/hooks/suggest-compact.sh",
            "timeout": 5,
            "async": true
          }
        ]
      }
    ],
    "PostToolUse": [
      {
        "matcher": "Edit|Write|MultiEdit",
        "hooks": [
          {
            "type": "command",
            "command": "${hook_path_prefix}/scripts/hooks/java-compile-check.sh",
            "timeout": 90
          },
          {
            "type": "command",
            "command": "${hook_path_prefix}/scripts/hooks/java-format.sh",
            "timeout": 60,
            "async": true
          }
        ]
      },
      {
        "hooks": [
          {
